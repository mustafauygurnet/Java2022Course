public class Main {
    public static void main(String[] args) throws Exception {
        
        int count = 0;

        //For
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        //While
        while (count < 10) {
            System.out.println(count);
            count++;
        }

        //Do While
        do{
            System.out.println(count);
            count++;
        }while(count < 10);


    }
}
